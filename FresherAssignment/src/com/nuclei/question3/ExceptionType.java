package com.nuclei.question3;

/**
 * The enum Exception type.
 */
public enum ExceptionType {
    /**
     * Generic exception type.
     */
    GENERIC,
    /**
     * Invalid input exception type.
     */
    INVALID_INPUT,

    /**
     * Node does not exist exception exception type.
     */
    NODE_DOES_NOT_EXIST_EXCEPTION,

    /**
     * Node exists exception exception type.
     */
    NODE_EXISTS_EXCEPTION,

    /**
     * Cyclic dependency exception exception type.
     */
    CYCLIC_DEPENDENCY_EXCEPTION
}

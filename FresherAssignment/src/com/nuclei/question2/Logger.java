public class Logger {

    /**
     * Log.
     */
    public static void log() {
        System.out.println();
    }

    /**
     * Log.
     *
     * @param message the message
     */
    public static void log(String message) {
        System.out.println(message);
    }

    public static void logp(String message) {
        System.out.print(message);
    }
}
